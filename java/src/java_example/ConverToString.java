package java_example;

public class ConverToString {
	public static void main(String[] args) {
		Object obj1=123;
		Object obj2=123.456;
		Object obj3="java";
		String str1=String.valueOf(obj1);
		String str2=String.valueOf(obj2);
		String str3=String.valueOf(obj3);
		System.out.println(str1+123);
		System.out.println(str2+123);
		System.out.println(str3+123);
		
	}

}
